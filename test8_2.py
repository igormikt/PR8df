import pandas as pd

# Создаем данные
data = {
    'Имя': [
        'Иван Петров', 'Мария Сидорова', 'Алексей Кузнецов', 'Ольга Иванова',
        'Дмитрий Смирнов', 'Екатерина Федорова', 'Сергей Волков',
        'Анна Морозова', 'Максим Попов', 'Наталья Васильева'
    ],
    'Математика': [5, 4, 3, 5, 4, 5, 3, 4, 2, 5],
    'Физика': [4, 5, 3, 4, 4, 5, 3, 4, 2, 5],
    'Литература': [3, 5, 4, 4, 3, 5, 4, 5, 3, 4],
    'История': [5, 4, 3, 4, 4, 5, 3, 5, 2, 4],
    'Информатика': [4, 5, 4, 5, 3, 5, 4, 5, 2, 5],
}

# Создаем DataFrame
df = pd.DataFrame(data)

# Вычисление квартилей, IQR и стандартного отклонения для Математики
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math
std_math = df['Математика'].std()

# Первые строки
print("Первые строки DataFrame:")
print(df.head(10), "\n")

# Средние значения
print("Средние оценки по предметам:")
print(df[['Математика', 'Физика', 'Литература', 'История', 'Информатика']].mean(), "\n")

# Медианные значения
print("Медианные оценки по предметам:")
print(df[['Математика', 'Физика', 'Литература', 'История', 'Информатика']].median(), "\n")

# Квартили, IQR и стандартное отклонение
print("Статистика по математике:")
print("Q1 (25-й перцентиль):", Q1_math)
print("Q3 (75-й перцентиль):", Q3_math)
print("IQR (межквартильный размах):", IQR_math)
print("Стандартное отклонение:", std_math)